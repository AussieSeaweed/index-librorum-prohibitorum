int N, M;
vector<int> graph[2001];

bool is_bipartite(int num_nodes, vector<int> graph[]) {
    bool visited[num_nodes], color[num_nodes];
    fill(visited, visited + num_nodes, false);
    queue<int> q;
    for (int source = 0; source < num_nodes; source++) {
        if (!visited[source]) {
            q.push(source);
            visited[source] = color[source] = true;

            while (!q.empty()) {
                int node = q.front();
                q.pop();

                for (int adj : graph[node]) {
                    if (!visited[adj]) {
                        color[adj] = !color[node];
                        visited[adj] = true;
                        q.push(adj);
                    } else if (color[node] == color[adj]) {
                        return false;
                    }
                }
            }
        }
    }
    return true;
}

