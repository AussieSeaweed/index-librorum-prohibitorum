using namespace std;

int pc[1000][1000];

void init_aux(int lo, int hi, int A[]) {
    if (lo == hi) {
        pc[lo][hi] = A[lo];
    } else {
        int mid = (lo + hi) / 2;
        init_aux(lo, mid, A);
        init_aux(mid + 1, hi, A);

        for (int i = mid; i >= lo; i--)
            if (pc[i][mid] == -1)
                pc[i][mid] = Secret(A[i], pc[i + 1][mid]);

        for (int i = mid + 1; i <= hi; i++)
            if (pc[mid + 1][i] == -1)
                pc[mid + 1][i] = Secret(pc[mid + 1][i - 1], A[i]);
    }
}

void Init(int N, int A[]) {
    memset(pc, -1, sizeof pc);
    init_aux(0, N - 1, A);
}

int Query(int L, int R) {
    for (int i = L; i < R; i++)
        if (~pc[L][i] && ~pc[i + 1][R])
            return Secret(pc[L][i], pc[i + 1][R]);

    assert(~pc[L][R]);
    return pc[L][R];
}