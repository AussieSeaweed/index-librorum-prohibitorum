using namespace std;

int N;
pair<double, double> points[15];

double dist(pair<double, double> a, pair<double, double> b) {
    double dx = a.first - b.first, dy = a.second - b.second;
    return sqrt(dx * dx + dy * dy);
}

double circumcircle_radius(double a, double b, double c) {
    double sp = (a + b + c) / 2;
    return (a * b * c) / (4 * sqrt(sp * (sp - a) * (sp - b) * (sp - c)));
}

int main() {
    scan(N);

    for (int i = 0; i < N; i++)
        scanf("%lf%lf", &points[i].first, &points[i].second);

    if (N == 1)
        printf("0\n");
    else if (N == 2) {
        printf("%.10lf\n", dist(points[0], points[1]) / 2);
    } else {
        double ans = 0;
        for (int i = 0; i < N; i++)
            for (int j = i + 1; j < N; j++)
                for (int k = j + 1; k < N; k++) {
                    double a = dist(points[i], points[j]), b = dist(points[j], points[k]), c = dist(points[k], points[i]), sp = (a + b + c) / 2;
                    if (sp == 0 || (a * a + b * b - c * c < 0) || (a * a + c * c - b * b < 0) || (b * b + c * c - a * a < 0))
                        ans = max(ans, max(a, b, c) / 2);
                    else
                        ans = max(ans, circumcircle_radius(a, b, c));
                }
        printf("%.10lf\n", ans);
    }

    return 0;
}