using namespace std;

int T, N = 122;
long long n, dp[122], cnt[122];

string get_nth_number(long long n) {
    int i = upper_bound(cnt, cnt + N, n) - cnt;
    string ret;
    n -= cnt[i - 1];

    while (i >= 2) {
        if (i == 2 || n >= dp[i - 3]) { // use 69
            n -= dp[i - 3];
            ret += "69";
            i -= 2;
        } else { // use 420
            ret += "420";
            i -= 3;
        }
    }

    return ret;
}

int main() {
    cnt[0] = dp[0] = 1;
    for (int i = 2; i < N; i++)
        dp[i] = dp[i - 2] + dp[i - 3];
    for (int i = 1; i < N; i++)
        cnt[i] += cnt[i - 1] + dp[i];
    scan(T);
    while (T--) {
        scan(n);
        cout <<  get_nth_number(n) << '\n';
    }
    return 0;
}