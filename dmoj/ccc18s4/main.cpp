#define MCONST 1000000
using namespace std;

long long dp1[MCONST];
unordered_map<int, long long> dp2;
int N;

long long solve(int n) {
    if (n < MCONST && ~dp1[n]) return dp1[n];
    else if (n >= MCONST && dp2.count(n)) return dp2[n];
    if (n == 1) return dp1[n] = 1;
    long long ret = 0;
    int sqrt_n = int(sqrt(n));

    for (int i = 1; i <= sqrt_n; i++) {
        ret += (n / i - n / (i + 1)) * solve(i);
    }

    for (int i = 2; i <= n; i++) {
        if (n / i <= sqrt_n) break;
        ret += solve(n / i);
    }

    if (n < MCONST) dp1[n] = ret;
    else dp2[n] = ret;
    return ret;
}

