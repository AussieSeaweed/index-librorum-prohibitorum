using namespace std;

int R, S, ans, valid[1000][1000], grid[1000][1000], values[1000];

int get_max_area() {
    int ret = 0;
    stack<int> s;

    for (int i = 0; i < S; i++) {
        while (!s.empty() && values[s.top()] >= values[i]) {
            int j = s.top();
            s.pop();
            ret = max(ret, (i - (s.empty() ? 0 : s.top() + 1) + 1) * (values[j] + 1));
        }

        s.push(i);
    }

    while (!s.empty()) {
        int i = s.top();
        s.pop();
        ret = max(ret, (S - (s.empty() ? 0 : s.top() + 1) + 1) * (values[i] + 1));
    }

    return ret;
}

int main() {
    scan(R);
    scan(S);

    for (int i = 0; i < R; i++) {
        for (int j = 0; j < S; j++) {
            scan(grid[i][j]);
            valid[i][j] = i && j && (grid[i - 1][j - 1] + grid[i][j] <= grid[i][j - 1] + grid[i - 1][j]);
        }
    }

    for (int i = 0; i < R; i++) {
        for (int j = 0; j < S; j++)
            values[j] = valid[i][j] ? values[j] + 1 : 0;
        ans = max(ans, get_max_area());
    }

    printf("%d\n", ans);

    return 0;
}