using namespace std;

int N, M, ending[200000], par[200001], ans[200000];
vector<int> intervals[200000];
priority_queue<int> pq;

int find_root(int node) { return par[node] == node ? node : par[node] = find_root(par[node]); }

void assign(int v) {
    for (int i = find_root(0); i < N; i = find_root(ending[i] + 1))
        par[i] = i + 1, ans[i] = v;
}

int main() {
    scan(N, M);

    for (int i = 0; i < M; i++) {
        int l, r;
        scan(l, r);
        intervals[--l].push_back(--r);
    }

    for (int i = 0; i < N; i++) {
        while (!pq.empty() && pq.top() <= i)
            pq.pop();

        for (int n : intervals[i])
            pq.push(n);

        ending[i] = pq.empty() ? i : pq.top();
    }

    for (int i = 0; i <= N; i++)
        par[i] = i;

    int val = 0;

    while (find_root(0) != N)
        assign(val++);

    for (int i = 0; i < N; i++) printf("%d ", ans[i] + 1);

    return 0;
}