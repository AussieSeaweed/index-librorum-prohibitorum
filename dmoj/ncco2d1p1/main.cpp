using namespace std;

int N, K;
long long T, x[301], dp[2][301][301], ans;
bool zero;

long long solve(bool dir, int i, int j) {
    if (~dp[dir][i][j]) return dp[dir][i][j];
    if (j - i == K) return dp[dir][i][j] = 0;
    dp[dir][i][j] = inf;
    long long cur_pos = dir ? x[j] : x[i];
    if (0 < i)
        dp[dir][i][j] = (K - j + i) * abs(cur_pos - x[i - 1]) + solve(false, i - 1, j);
    if (j < N - 1)
        dp[dir][i][j] = min(dp[dir][i][j], (K - j + i) * abs(cur_pos - x[j + 1]) + solve(true, i, j + 1));
    return dp[dir][i][j];
}

int main() {
    scan(N);
    scan(T);
    for (int i = 0; i < N; i++) { scan(x[i]); if (x[i] == 0) zero = true; }
    if (!zero) N++;
    sort(x, x + N);
    int zero_index = -1;
    for (int i = 0; i < N; i++) { if (x[i] == 0) { zero_index = i; break; } }
    for (K = 1; K <= N - zero; K++) {
        memset(dp, -1, sizeof dp);
        ans = max(ans, T * K - solve(true, zero_index, zero_index));
    }
    printf("%lld\n", (zero ? T : 0) + ans);
    return 0;
}