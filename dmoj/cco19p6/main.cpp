#define INF 0x3f3f3f3f3f3f3f3fll
#define inf 0x3f3f3f3f
#define MAX_SEARCH_DEPTH 100
using namespace std;

int N, M, ans = inf;
unordered_set<string> sequences;
unordered_map<string, vector<string>> ps;
unordered_map<string, int> dp[MAX_SEARCH_DEPTH];

int solve(int search_depth, string dif) {
    if (search_depth == MAX_SEARCH_DEPTH) return inf;
    if (dp[search_depth].count(dif)) return dp[search_depth][dif];
    if (dif.empty()) return dp[search_depth][dif] = 0;

    int ret = inf;

    if (search_depth)
        for (int i = 1; i <= dif.size(); i++)
            if (sequences.count(dif.substr(0, i)))
                ret = min(ret, i + solve(search_depth + 1, dif.substr(i)));

    for (string &s : ps[dif])
        ret = min(ret, (int) dif.size() + solve(search_depth + 1, s));

    return dp[search_depth][dif] = ret;
}

int main() {
    scan(N, M);

    for (int i = 0; i < N; i++) {
        string s;
        cin >> s;
        sequences.insert(s);
        for (int j = 1; j <= s.size() - 1; j++) {
            ps[s.substr(0, j)].push_back(s.substr(j));
        }
    }

    for (string s : sequences)
        ans = min(ans, solve(0, s));

    printf("%d\n", ans == inf ? -1 : ans);

    return 0;
}